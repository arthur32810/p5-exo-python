def sum(a, b):
    """Somme de deux nombres
    Args:
        a (int): premier nombre
        b (int): deuxième nombre
    Returns:
        int : somme de a et b
    """
    return a + b


def subtraction(a, b):
    """Soustraction de deux nombres
    Args:
        a (int): premier nombre
        b (int): deuxième nombre
    Returns:
        int : soustraction de a et b"""
    return a - b
